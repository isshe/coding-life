#!/bin/bash

# 音频文件名前缀删除工具
#
# 这个脚本用于删除音频文件名前面的数字和点号前缀，主要功能包括：
# - 删除文件名开头的数字
# - 删除文件名开头的点号
# - 删除文件名开头的空格
#
# 使用方法：
# 将脚本放在需要处理的音频文件目录下直接运行
#
# 处理规则：
# - 删除文件名开头的数字序号（如"01. "、"1."等）
# - 删除文件名开头的点号
# - 删除文件名开头的空格
# - 保持其他部分不变

for file in *; do
    # new_file="${file#[0-9]*[. ]*}"
    new_file="$(echo "$file" | sed -E 's/^[0-9]*\.?[ ]*//g')"
    if [ "$file" != "$new_file" ]; then
        echo "Rename '$file' to '$new_file'"
        mv "$file" "$new_file"
    fi
done
